/**
 * list의 마지막 노드 삭제
 */
int deleteLast(headNode* h) {
	listNode* toDelete;
	listNode* secondLastNode;

	    if( h->first == NULL)
	    {
	        printf("List is already empty.");
	    }
	    else
	    {
	        toDelete =  h->first;
	        secondLastNode =  h->first;

	        /* 마지막 노드 탐색 */
	        while(toDelete->link != NULL)
	        {
	            secondLastNode = toDelete;
	            toDelete = toDelete->link;
	        }

	        if(toDelete == h->first)
	        {
	        	h->first = NULL;
	        }
	        else
	        {
	            /* 마지막노드 앞 노드와 마지막 노드의 연결 해제  */
	            secondLastNode->link = NULL;
	        }

	        /*마지막 노드 해제 */
	        free(toDelete);

	        printf("SUCCESSFULLY DELETED LAST NODE OF LIST\n");
	    }

	return 0;
}


/**
 * 리스트의 링크를 역순으로 재 배치
 */
int invertList(headNode* h) {
	listNode* preNode, *sNode;

	    if( h->first != NULL)
	    {
	        preNode = h->first;
	        sNode =h->first->link;
	        h->first= h->first->link;

	        preNode->link = NULL; // 첫번째 노드를 마지막 노드로 전환

	        while(h->first != NULL)
	        {
	        	h->first = h->first->link;
	            sNode->link = preNode;

	            preNode =sNode;
	            sNode = h->first;
	        }

	        h->first = preNode; // 마지막 노드를 첫번째 노드로 전환

	        printf("SUCCESSFULLY REVERSED LIST\n");
	    }

	return 0;
}


